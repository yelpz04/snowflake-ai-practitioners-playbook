-- ChurnRadar Setup SQL — App 3 of 10
-- Snowpark ML + Cortex AI + Semantic Views → Customer Churn Prediction

CREATE DATABASE IF NOT EXISTS CHURNRADAR; CREATE SCHEMA IF NOT EXISTS CHURNRADAR.PUBLIC;
USE DATABASE CHURNRADAR; USE SCHEMA PUBLIC;

-- Core tables
CREATE OR REPLACE TABLE CUSTOMERS (
    CUSTOMER_ID     STRING NOT NULL, CUSTOMER_NAME STRING, INDUSTRY STRING,
    COMPANY_SIZE    STRING, REGION STRING, CONTRACT_VALUE FLOAT,
    CREATED_DATE    DATE,  CHURNED_DATE DATE, CHURNED BOOLEAN DEFAULT FALSE,
    INGESTED_AT     TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);
CREATE OR REPLACE TABLE ORDERS (
    ORDER_ID STRING NOT NULL, CUSTOMER_ID STRING, ORDER_DATE DATE,
    AMOUNT FLOAT, STATUS STRING, INGESTED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);
CREATE OR REPLACE TABLE SUPPORT_TICKETS (
    TICKET_ID STRING NOT NULL, CUSTOMER_ID STRING, STATUS STRING,
    CREATED_DATE DATE, RESOLVED_DATE DATE,
    INGESTED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);
CREATE OR REPLACE TABLE CALL_INSIGHTS (
    INSIGHT_ID STRING NOT NULL, CUSTOMER_ID STRING, CALL_DATE DATE,
    SENTIMENT_SCORE FLOAT, CHURN_RISK_SCORE FLOAT,
    INGESTED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- ML feature view
CREATE OR REPLACE VIEW ML_FEATURES AS
SELECT
    c.CUSTOMER_ID, c.CUSTOMER_NAME, c.INDUSTRY, c.COMPANY_SIZE, c.REGION,
    c.CONTRACT_VALUE, c.CHURNED,
    DATEDIFF('day', MAX(o.ORDER_DATE), CURRENT_DATE())       AS days_since_last_order,
    COUNT(DISTINCT t.TICKET_ID)                              AS open_tickets,
    COALESCE(AVG(ci.SENTIMENT_SCORE), 0)                     AS avg_sentiment_score,
    COUNT(DISTINCT ci.INSIGHT_ID)                            AS total_calls,
    COALESCE(AVG(DATEDIFF('day', t.CREATED_DATE,
        COALESCE(t.RESOLVED_DATE, CURRENT_DATE()))), 0)      AS resolution_time_avg
FROM CUSTOMERS c
LEFT JOIN ORDERS          o  ON c.CUSTOMER_ID = o.CUSTOMER_ID
LEFT JOIN SUPPORT_TICKETS t  ON c.CUSTOMER_ID = t.CUSTOMER_ID AND t.STATUS = 'OPEN'
LEFT JOIN CALL_INSIGHTS   ci ON c.CUSTOMER_ID = ci.CUSTOMER_ID
GROUP BY c.CUSTOMER_ID, c.CUSTOMER_NAME, c.INDUSTRY, c.COMPANY_SIZE,
         c.REGION, c.CONTRACT_VALUE, c.CHURNED;

-- Predictions table
CREATE OR REPLACE TABLE CUSTOMER_HEALTH_SCORES (
    CUSTOMER_ID     STRING, CUSTOMER_NAME STRING, RISK_LEVEL STRING,
    CHURN_PROBABILITY FLOAT, CHURN_PREDICTION BOOLEAN,
    AVG_SENTIMENT   FLOAT,  OPEN_TICKETS INT, DAYS_SINCE_ORDER INT,
    AI_EXPLANATION  STRING, SCORED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Semantic View for Cortex Agent natural language queries
CREATE OR REPLACE SEMANTIC VIEW CHURN_INTELLIGENCE_SV
  COMMENT = 'Customer health and churn risk — queryable via natural language'
  TABLES (
    TABLE CUSTOMER_HEALTH_SCORES AS HEALTH
      PRIMARY KEY (CUSTOMER_ID)
      COMMENT = 'ML-scored churn predictions, refreshed weekly'
  )
  DIMENSIONS (
    HEALTH.CUSTOMER_NAME COMMENT = 'Customer company name',
    HEALTH.RISK_LEVEL    COMMENT = 'High, Medium, or Low churn risk',
    HEALTH.SCORED_AT     COMMENT = 'When this prediction was generated'
  )
  METRICS (
    MEASURE HEALTH.CHURN_PROBABILITY
      COMMENT = 'Probability of churn in next 30 days (0.0 to 1.0)'
      DEFAULT AGGREGATION = AVG,
    MEASURE HEALTH.OPEN_TICKETS
      COMMENT = 'Number of currently open support tickets'
      DEFAULT AGGREGATION = SUM
  );

