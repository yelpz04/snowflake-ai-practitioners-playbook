CREATE OR REPLACE SEMANTIC VIEW SALES_INTELLIGENCE_SV
    TABLES (
        orders   AS NL_BI.PUBLIC.ORDERS   PRIMARY KEY (ORDER_ID),
        products AS NL_BI.PUBLIC.PRODUCTS PRIMARY KEY (PRODUCT_ID),
        regions  AS NL_BI.PUBLIC.REGIONS  PRIMARY KEY (REGION_ID)
    )
    RELATIONSHIPS (
        orders (PRODUCT_ID) REFERENCES products,
        orders (REGION_ID)  REFERENCES regions
    )
    FACTS    (orders.ORDER_VALUE, orders.QUANTITY, orders.DISCOUNT_AMOUNT)
    DIMENSIONS (orders.ORDER_DATE, orders.STATUS,
                products.PRODUCT_NAME, products.CATEGORY,
                regions.REGION_NAME, regions.COUNTRY)
    METRICS  (
        total_revenue   AS SUM(orders.ORDER_VALUE),
        total_orders    AS COUNT(orders.ORDER_ID),
        avg_order_value AS AVG(orders.ORDER_VALUE)
    );
