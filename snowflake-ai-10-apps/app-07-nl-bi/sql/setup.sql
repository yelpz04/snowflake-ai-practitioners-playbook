CREATE DATABASE IF NOT EXISTS NL_BI; CREATE SCHEMA IF NOT EXISTS NL_BI.PUBLIC;
USE DATABASE NL_BI; USE SCHEMA PUBLIC;
CREATE STAGE IF NOT EXISTS ANALYST_STAGE;

CREATE OR REPLACE TABLE ORDERS (
    ORDER_ID STRING DEFAULT UUID_STRING(), ORDER_DATE DATE,
    CUSTOMER_ID STRING, PRODUCT_ID STRING, REGION_ID STRING,
    ORDER_VALUE FLOAT, QUANTITY INT, DISCOUNT_AMOUNT FLOAT DEFAULT 0,
    STATUS STRING DEFAULT 'COMPLETED'
);
CREATE OR REPLACE TABLE PRODUCTS (
    PRODUCT_ID STRING DEFAULT UUID_STRING(), PRODUCT_NAME STRING,
    CATEGORY STRING, UNIT_PRICE FLOAT
);
CREATE OR REPLACE TABLE REGIONS (
    REGION_ID STRING DEFAULT UUID_STRING(), REGION_NAME STRING, COUNTRY STRING
);

